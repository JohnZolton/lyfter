import { type NextPage } from "next";
import Head from "next/head";
import { api } from "~/utils/api";
import React, {
  useState,
} from "react";
import {
  SignedIn,
  SignedOut,
  SignInButton,
} from "@clerk/nextjs";
import type {
  ActualWorkout,
  ActualExercise,
  exerciseSet,
  WorkoutPlanTwo,
} from "@prisma/client";
import { NavBar } from "~/pages/components/navbar";
import  PageLayout  from "~/pages/components/pagelayout";
import LoadingSpinner from "./components/loadingspinner";
import MenuLayout from "./components/menulayout";
import PerformanceWarning from "./components/performancewarning";

const Home: NextPage = () => {
  return (
    <>
      <Head>
        <title>Lyfter</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <PageLayout>
        <NavBar />

        <SignedIn>
          <br></br>
          <Content />
          <br></br>
          <div></div>
        </SignedIn>
        <SignedOut>
          {/* Signed out users get sign in button */}
          <SignInButton redirectUrl="home">
            <button className="rounded-full bg-white p-3 text-xl text-black">
              Sign In
            </button>
          </SignInButton>
        </SignedOut>
      </PageLayout>
    </>
  );
};

export default Home;

function Content() {

  return (
    <div className="flex flex-col items-center w-full">
      <Workouts />
    </div>
  );
}

function Workouts() {
  const { data: workoutPlans, isLoading: workoutsLoading } =
    api.getWorkouts.getPlanByUserId.useQuery();

  const [selectedPlan, setSelectedPlan] = useState<
    WorkoutPlanTwo & {
      workouts: (ActualWorkout & {
        exercises: (ActualExercise & {
          sets: (exerciseSet & { priorSet: exerciseSet | null })[];
        })[];
      })[];
    }
  >();

  if (workoutsLoading) {
    return <LoadingSpinner />;
  }

  if (!workoutPlans || workoutPlans === undefined) {
    return <div>No Workout History</div>;
  }

  const handleButtonClick = (
    plan: WorkoutPlanTwo & {
      workouts: (ActualWorkout & {
        exercises: (ActualExercise & {
          sets: (exerciseSet & { priorSet: exerciseSet | null })[];
        })[];
      })[];
    }
  ) => {
    setSelectedPlan(plan);
  };

  return (
    <div className="flex flex-wrap w-full pt-5">
        {selectedPlan && <DisplayPlan plan={selectedPlan} />}
      <div className="w-full">

        {!selectedPlan && <div className="mb-4 text-center text-2xl font-bold ">Workout Plans: </div>}
      {!selectedPlan && <MenuLayout>
        {!selectedPlan &&
          workoutPlans.map((plan) => (
            <div key={plan.planId} className="px-4 my-2 flex flex-row items-center justify-between w-full">
                <div className="text-lg font-semibold text-slate-100">
                {plan.description}
                  </div>
                <button
              className="m-1 inline-flex items-center rounded bg-green-600 px-2 py-1 font-bold text-white hover:bg-green-500"
                 onClick={() => handleButtonClick(plan)}>Select</button>
            </div>
          ))}
      </MenuLayout>}

        {selectedPlan && <div className="flex justify-center items-center w-full pb-10">
           <button
      className=" flex justify-center items-center rounded bg-green-600 px-2 py-1 font-bold text-white hover:bg-green-500"
         onClick={() => setSelectedPlan(undefined)}>Back</button></div>}
      </div>
    </div>
  );
}

interface DisplayPlanProps {
  plan:
    | (WorkoutPlanTwo & {
        workouts: (ActualWorkout & {
          exercises: (ActualExercise & {
            sets: (exerciseSet & { priorSet: exerciseSet | null })[];
          })[];
        })[];
      })
    | undefined;
}

interface SortedWorkouts {
  Sunday: (ActualWorkout & {
    exercises: (ActualExercise & {
      sets: (exerciseSet & { priorSet: exerciseSet | null })[];
    })[];
  })[];
  Monday: (ActualWorkout & {
    exercises: (ActualExercise & {
      sets: (exerciseSet & { priorSet: exerciseSet | null })[];
    })[];
  })[];
  Tuesday: (ActualWorkout & {
    exercises: (ActualExercise & {
      sets: (exerciseSet & { priorSet: exerciseSet | null })[];
    })[];
  })[];
  Wednesday: (ActualWorkout & {
    exercises: (ActualExercise & {
      sets: (exerciseSet & { priorSet: exerciseSet | null })[];
    })[];
  })[];
  Thursday: (ActualWorkout & {
    exercises: (ActualExercise & {
      sets: (exerciseSet & { priorSet: exerciseSet | null })[];
    })[];
  })[];
  Friday: (ActualWorkout & {
    exercises: (ActualExercise & {
      sets: (exerciseSet & { priorSet: exerciseSet | null })[];
    })[];
  })[];
  Saturday: (ActualWorkout & {
    exercises: (ActualExercise & {
      sets: (exerciseSet & { priorSet: exerciseSet | null })[];
    })[];
  })[];
}

function DisplayPlan({ plan }: DisplayPlanProps) {
  const [workoutList, setWorkoutList] = useState<SortedWorkouts>();
  if (!plan) {
    return <div></div>;
  }

  function sortWorkoutsByDay(
    workouts: (ActualWorkout & {
      exercises: (ActualExercise & {
        sets: (exerciseSet & { priorSet: exerciseSet | null })[];
      })[];
    })[]
  ): SortedWorkouts {
    const sortedWorkouts: SortedWorkouts = {
      Sunday: [],
      Monday: [],
      Tuesday: [],
      Wednesday: [],
      Thursday: [],
      Friday: [],
      Saturday: [],
    };
    for (const workout of workouts) {
      sortedWorkouts[workout.nominalDay as keyof SortedWorkouts].push(workout);
    }
    return sortedWorkouts;
  }

  if (!workoutList) {
    setWorkoutList(sortWorkoutsByDay(plan.workouts));
  }

  return (
    <div className="mx-auto max-w-3xl flex-col">
      {workoutList &&
        Object.keys(workoutList).map((day, idx) => (
          <div key={"base" + idx.toString()}>
            {workoutList[day as keyof typeof workoutList][0]?.description && (
              <div
                key={day}
                className="mb-4 flex flex-col rounded-md bg-slate-800 p-4 shadow-md"
              >
                <div className="mb-4 text-center text-3xl font-bold">{day}</div>
                <div
                  className="overflow-x-auto bg-slate-900"
                  style={{ maxWidth: "100vw" }}
                >
                  <table className="table-fixed whitespace-nowrap">
                    <thead>
                      <tr>
                        <th className="w-1/4">
                          {
                            workoutList[day as keyof typeof workoutList][0]
                              ?.description
                          }
                        </th>
                        {workoutList[day as keyof typeof workoutList].map(
                          (workout, workoutCount) => (
                            <th className="w-1/4" key={workout.workoutId}>
                              Week {workoutCount + 1}
                            </th>
                          )
                        )}
                      </tr>
                    </thead>
                    <tbody>
                      {workoutList[
                        day as keyof typeof workoutList
                      ][0]?.exercises.map((exercise, index) => (
                        <tr key={index}>
                          <td className="border px-4 py-2">
                            {exercise.description}
                          </td>
                          {workoutList[day as keyof typeof workoutList].map(
                            (workout) => (
                              <td
                                className="border px-3 py-2"
                                key={workout.workoutId}
                              >
                                {workout.exercises[index]?.sets.map(
                                  (set, setIndex) => (
                                    <div
                                        className="flex items-center justify-between"
                                      key={set.setId}
                                    >
                                      <div>
                                        Set {setIndex + 1}: {set.weight}lbs x{" "}
                                        {set.reps} @ {set.rir} RIR
                                      </div>
                                      <div className="pl-2">
                                        <PerformanceWarning priorSet={set.priorSet} currentSet={set}/>
                                      </div>
                                    </div>
                                  )
                                )}
                              </td>
                            )
                          )}
                        </tr>
                      ))}
                    </tbody>
                  </table>
                </div>
              </div>
            )}
          </div>
        ))}
    </div>
  );
}
